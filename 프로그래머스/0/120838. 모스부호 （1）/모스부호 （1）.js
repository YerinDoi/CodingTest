function solution(letter) {
    const morse = {
        ".-": "a", "-...": "b", "-.-.": "c", "-..": "d", ".": "e",
        "..-.": "f", "--.": "g", "....": "h", "..": "i", ".---": "j",
        "-.-": "k", ".-..": "l", "--": "m", "-.": "n", "---": "o",
        ".--.": "p", "--.-": "q", ".-.": "r", "...": "s", "-": "t",
        "..-": "u", "...-": "v", ".--": "w", "-..-": "x", "-.--": "y",
        "--..": "z"
    };

    return letter.split(" ") // 모스부호를 공백 기준으로 나눔
                 .map(code => morse[code]) // 변환
                 .join(""); // 문자열로 합침
}

